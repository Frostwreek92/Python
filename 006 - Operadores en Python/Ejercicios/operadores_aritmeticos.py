# Operadores Aritmeticos

a = 10
b = 3

# Suma +
suma = a + b
print(f'Suma de {a} + {b} = {suma}')

# Resta -
resta = a - b
print(f'Resta de {a} - {b} = {resta}')

# Multiplicaci√≥n *
multiplicacion = a * b
print(f'Multiplicacion de {a} * {b} = {multiplicacion}')

# Division /
division = a / b
print(f'Division de {a} / {b} = {division:.2f}')

# Division entera
division_entera = a // b
print(f'Division entera de {a} // {b} = {division_entera}')

# Resto de la division
resto = a % b
print(f'Residuo de {a} / {b} = {resto}')

# Exponente (potencia) **
potencia = a ** b
print(f'Potencia de {a} elevado a {b} = {potencia}')